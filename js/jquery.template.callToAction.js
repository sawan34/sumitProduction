;(function($,doc,win) 
{
	"use strict";
	
	var templateCallToAction=function(object,option)
	{
		/**********************************************************************/
		
		var $this=$(object);
		var $option=option;

		/**********************************************************************/

		this.build=function() 
		{
			$this.responsiveElement({width:650,children:null,className:'template-call-to-action-responsive'});
		};
		
		/**********************************************************************/
	}
	
	/**************************************************************************/
	
	$.fn.templateCallToAction=function(option) 
	{
		return this.each(function() 
		{
			var object=new templateCallToAction(this,option);
			object.build();
			
			return(object);
		});
	};
	
	/**************************************************************************/

})(jQuery,document,window);

